const mongoose = require("mongoose");

//1. create a schema
const User = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

//2. create Model
module.exports = mongoose.model("User", User);
